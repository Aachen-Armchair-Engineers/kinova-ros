/**
 * @brief obtain KinovaPosefrom geometry_msgs::Pose.
 *
 * obtain KinovaPose[X,Y,Z,ThetaX,ThetaY,ThetaZ] from geometry_msgs::Pose[x,y,z,qx,qy,qz,qw].
 * Euler angles[] are extracted from tf::Matrix3x3 EulerYPR = Rz(tz)*Ry(ty)*Rx(tx), where EulerYPR is generated by Quaterion.
 * Units are in meters and radians, The range of Euler angles are [-M_PI, M_PI)
 *
 * @param geometry_msgs ROS standard pose[x,y,z,qx,qy,qz,qw], in meters and quaterion.
 */
KinovaPose::KinovaPose(const geometry_msgs::Pose &pose)
{
    double tx, ty, tz;
    tf::Quaternion q;
    tf::quaternionMsgToTF(pose.orientation, q);

    tf::Matrix3x3 bt_q(q);

    // tf::Matrix3x3 EulerYPR = Rz(tz)*Ry(ty)*Rx(tx)
    bt_q.getEulerYPR(tz, ty, tx);

    X = static_cast<float>(pose.position.x);
    Y = static_cast<float>(pose.position.y);
    Z = static_cast<float>(pose.position.z);

    ThetaX = normalizeInRads(tx);
    ThetaY = normalizeInRads(ty);
    ThetaZ = normalizeInRads(tz);
}

/**
 * @brief obtain KinovaPosefrom CartesianInfo &pose.
 *
 * KinovaPose[X,Y,Z,ThetaX,ThetaY,ThetaZ]. position are in meters, and orientation is Euler-ZYX = Rz(ThetaZ)*Ry(ThetaY)*Rx(ThetaX). The range of Euler angles are [-M_PI, M_PI)
 *
 * @param pose CartesianInfo units are in meters and degrees
 */
KinovaPose::KinovaPose(const CartesianInfo &pose)
{
    X = pose.X;
    Y = pose.Y;
    Z = pose.Z;

    ThetaX = normalizeInRads(pose.ThetaX);
    ThetaY = normalizeInRads(pose.ThetaY);
    ThetaZ = normalizeInRads(pose.ThetaZ);
}

/**
 * @brief construct geometric::Pose message from KinovaPose
 * @return geometry_msgs::Pose[x,y,z,qx,qy,qz,qw] position in meters, orientation is in Quaternion.
 */
geometry_msgs::Pose KinovaPose::constructPoseMsg()
{
    geometry_msgs::Pose pose;
    tf::Quaternion position_quaternion;

   // setEulerZYX(tz, ty, tx) == setRPY(tx, ty, tz) == = mz * my * mx. the previous is Euler angle rotating with temp axis, while latter rotates with fixed axis.
//  position_quaternion.setEulerZYX(ThetaZ, ThetaY, ThetaX);
    position_quaternion.setRPY(ThetaX, ThetaY, ThetaZ);

    tf::quaternionTFToMsg(position_quaternion, pose.orientation);

    pose.position.x = X;
    pose.position.y = Y;
    pose.position.z = Z;

    return pose;
}

/**
 * @brief construct geometric::Wrench message from KinovaPose data structure
 *
 * KinovaPose[X,Y,Z,ThetaX,ThetaY,ThetaZ] doesn't store pose information in this case. Instead, it stores force and torque correspondingly.
 *
 * @return geometry_msgs [Fx,Fy,Fz,Tx,Ty,Tz] in Newton and Nm.
 */
geometry_msgs::Wrench KinovaPose::constructWrenchMsg()
{
    geometry_msgs::Wrench wrench;

    wrench.force.x  = X;
    wrench.force.y  = Y;
    wrench.force.z  = Z;
    wrench.torque.x = ThetaX;
    wrench.torque.y = ThetaY;
    wrench.torque.z = ThetaZ;

    return wrench;
}

bool KinovaPose::isCloseToOther(const KinovaPose &other, float position_tolerance, float EulerAngle_tolerance) const
{
    bool status = true;
    status = status && areValuesClose(X, other.X, position_tolerance);
    status = status && areValuesClose(Y, other.Y, position_tolerance);
    status = status && areValuesClose(Z, other.Z, position_tolerance);
    status = status && areValuesClose(ThetaX, other.ThetaX, EulerAngle_tolerance);
    status = status && areValuesClose(ThetaY, other.ThetaY, EulerAngle_tolerance);
    status = status && areValuesClose(ThetaZ, other.ThetaZ, EulerAngle_tolerance);
    return status;
}

/**
 * @brief KinovaAngles::KinovaAngles
 *
 *
 *
 * @param angles in radians, input has no limit, but output KinovaAngles are limited to [0,360)
 * @param j6o is 270 for all the robot model, except the 1st generation of jaco robot (260)
 */
KinovaAngles::KinovaAngles(const kinova_msgs::JointAngles &angles, double j6o)
{
    Actuator1 = normalizePositiveInDegrees(180.0 - (angles.joint1 * (180.0 / M_PI)));
    Actuator2 = normalizePositiveInDegrees((angles.joint2 * (180.0 / M_PI)) + 270.0);
    Actuator3 = normalizePositiveInDegrees(90.0 - (angles.joint3 * (180.0 / M_PI)));
    Actuator4 = normalizePositiveInDegrees(180.0 - (angles.joint4 * (180.0 / M_PI)));
    Actuator5 = normalizePositiveInDegrees(180.0 - (angles.joint5 * (180.0 / M_PI)));
    Actuator6 = normalizePositiveInDegrees(j6o   - (angles.joint6 * (180.0 / M_PI)));
}


KinovaAngles::KinovaAngles(const AngularInfo &angles)
{
    Actuator1 = normalizePositiveInDegrees(angles.Actuator1);
    Actuator2 = normalizePositiveInDegrees(angles.Actuator2);
    Actuator3 = normalizePositiveInDegrees(angles.Actuator3);
    Actuator4 = normalizePositiveInDegrees(angles.Actuator4);
    Actuator5 = normalizePositiveInDegrees(angles.Actuator5);
    Actuator6 = normalizePositiveInDegrees(angles.Actuator6);
}


kinova_msgs::JointAngles KinovaAngles::constructAnglesMsg(double j6o)
{
    kinova_msgs::JointAngles angles;
    angles.joint1 = (180.0 - Actuator1) / (180.0 / M_PI);
    angles.joint2 = (Actuator2 - 270.0) / (180.0 / M_PI);
    angles.joint3 = (90.0 - Actuator3) / (180.0 / M_PI);
    angles.joint4 = (180.0 - Actuator4) / (180.0 / M_PI);
    angles.joint5 = (180.0 - Actuator5) / (180.0 / M_PI);
    angles.joint6 = (j6o   - Actuator6) / (180.0 / M_PI);
    return angles;
}


bool KinovaAngles::isCloseToOther(const KinovaAngles &other, float tolerance) const
{
    bool status = true;
    status = status && areValuesClose(Actuator1, other.Actuator1, tolerance);
    status = status && areValuesClose(Actuator2, other.Actuator2, tolerance);
    status = status && areValuesClose(Actuator3, other.Actuator3, tolerance);
    status = status && areValuesClose(Actuator4, other.Actuator4, tolerance);
    status = status && areValuesClose(Actuator5, other.Actuator5, tolerance);
    status = status && areValuesClose(Actuator6, other.Actuator6, tolerance);
    return status;
}


FingerAngles::FingerAngles(const kinova_msgs::FingerPosition &position)
{
    Finger1 = position.finger1;
    Finger2 = position.finger2;
    Finger3 = position.finger3;
}


FingerAngles::FingerAngles(const FingersPosition &angle)
{
    Finger1 = angle.Finger1;
    Finger2 = angle.Finger2;
    Finger3 = angle.Finger3;
}


kinova_msgs::FingerPosition FingerAngles::constructFingersMsg()
{
    kinova_msgs::FingerPosition angles;
    angles.finger1 = Finger1;
    angles.finger2 = Finger2;
    angles.finger3 = Finger3;
    return angles;
}


bool FingerAngles::isCloseToOther(const FingerAngles &other, float tolerance) const
{
    bool status = true;
    status = status && areValuesClose(Finger1, other.Finger1, tolerance);
    status = status && areValuesClose(Finger2, other.Finger2, tolerance);
    status = status && areValuesClose(Finger3, other.Finger3, tolerance);
    return status;
}

