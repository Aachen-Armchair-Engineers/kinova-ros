KinovaCommException::KinovaCommException(const std::string& message, const int error_code)
{
    std::stringstream ss;
        ss << "KinovaCommException: " << message << " (return code: " << error_code << ")" << std::endl;
    desc_ = ss.str();
}


const char* KinovaCommException::what() const throw()
{
    return desc_.c_str();
}


